import type { LoopSpec } from "@/lib/types";
import { designFromSpec } from "./architect";

/**
 * CODEGEN AGENT (builder 3/3)
 * Emits a runnable Claude Code project from the LoopSpec — the artifact the
 * user keeps. Deterministic template-fill (PLAN.md §13): a fixed scaffold where
 * the spec is interpolated into the policy, rubric, and tool wiring. No model
 * call, so it's instant and never produces broken code.
 *
 * The generated project is GENUINELY RUNNABLE: `npm test` passes with ZERO npm
 * install and ZERO network (Node's built-in `node --test` runner + pure ES
 * modules). The SDK is only needed for `npm run loop`. This function is pure:
 * it returns in-memory strings and reads no template files, so it works inside
 * a Next.js serverless route.
 */

export interface GeneratedFile {
  path: string;
  content: string;
}

function slug(s: string): string {
  return s.toLowerCase().replace(/[^a-z0-9]+/g, "-").replace(/(^-|-$)/g, "");
}

export function generateRepo(spec: LoopSpec): GeneratedFile[] {
  const name = slug(spec.name) || "operating-loop";
  const { rubricMd } = designFromSpec(spec);

  const claudeMd = [
    `# ${spec.name}`,
    ``,
    `> ${spec.description}`,
    ``,
    `A self-improving operating loop generated by Loopsmith. It runs the closed loop:`,
    `**sensor → policy → tools → gate → learning** — and gets better every run.`,
    ``,
    `## How it runs`,
    `- **Cadence:** \`${spec.cadence}\` (a scheduler fires \`npm run loop\` on this cadence).`,
    `- **Sensors:** ${spec.sensors.join(", ")} (via MCP connectors).`,
    `- **Decision policy:** ${spec.decisionPolicy}`,
    `- **Output:** ${spec.outputFormat}`,
    `- **Gate:** every result is graded against \`rubric.md\`; below 80/100 it is held for review.`,
    `- **Memory:** durable lessons in \`memory/\` are read first on each run.`,
    ``,
    `## Layers`,
    `| Layer | Job | File |`,
    `|---|---|---|`,
    `| Sensor | ingest + normalize signals | \`engine/loop.mjs\` (sensor step) |`,
    `| Policy | decide what matters (Opus 4.8) | \`engine/loop.mjs\` (policy step) |`,
    `| Tools | take action (dry-run by default) | \`engine/tools.mjs\` |`,
    `| Gate | grade vs \`rubric.md\` (Opus 4.8) | \`engine/gate.mjs\` |`,
    `| Learning | reflect → \`memory/\` (Opus 4.8) | \`engine/loop.mjs\` (learning step) |`,
    ``,
    `## Run`,
    `\`\`\`bash`,
    `npm test                       # works immediately — no install, no network`,
    ``,
    `export ANTHROPIC_API_KEY=...   # Opus 4.8 (only needed for the live loop)`,
    `npm install                    # only needed for the live loop`,
    `npm run loop                   # one live run`,
    `npm run health -- --once       # print health JSON and exit`,
    `\`\`\``,
    ``,
    `If \`ANTHROPIC_API_KEY\` is not set, \`npm run loop\` prints a friendly message`,
    `and exits cleanly — safe to explore.`,
    ``,
    `Generated by Loopsmith · powered by Claude Opus 4.8.`,
  ].join("\n");

  const runbook = [
    `# Runbook — ${spec.name}`,
    ``,
    `## What this does`,
    `${spec.description}`,
    ``,
    `## The loop, in plain English`,
    `1. **Listens** to ${spec.sensors.join(", ")}.`,
    `2. **Decides** what matters: ${spec.decisionPolicy}`,
    `3. **Acts**: ${spec.outputFormat} (drafts you review).`,
    `4. **Checks** the result against the quality bar (see rubric.md).`,
    `5. **Learns** your preferences and applies them next time.`,
    ``,
    `## Schedule`,
    `Runs \`${spec.cadence}\`. Toggle off anytime; nothing sends without your OK while in dry-run.`,
    ``,
    `## What "done" means (verifiable by the model, no human)`,
    `- \`npm test\` passes immediately with no install and no network.`,
    `- \`npm run loop\` exits 0 and writes \`runs/<ts>/output.md\` + \`runs/<ts>/gate.json\`.`,
    `- The gate score is present and numeric.`,
    `- A new lesson appears in \`memory/\` (or the score is non-decreasing on fixed input).`,
    `- \`npm run health -- --once\` prints \`{status:"ok", ...}\`.`,
  ].join("\n");

  const pkg = JSON.stringify(
    {
      name,
      version: "0.1.0",
      private: true,
      type: "module",
      scripts: {
        test: "node --test",
        loop: "node engine/loop.mjs",
        health: "node health.mjs",
      },
      dependencies: { "@anthropic-ai/sdk": "^0.104.1" },
    },
    null,
    2,
  );

  // ── engine/gate.mjs — identical logic to engine/gate.ts (no imports, no SDK).
  const gateMjs = [
    `/**`,
    ` * QUALITY-GATE LAYER (4/5) — pure ES module, no imports, no SDK.`,
    ` * "Fit to operator" is computed deterministically from accumulated memory:`,
    ` * more applied lessons → higher fit → higher total, run over run. This makes`,
    ` * the self-improvement signal robust and reproducible.`,
    ` */`,
    ``,
    `export const PASS_THRESHOLD = 80;`,
    `const FIT_WEIGHT = 25;`,
    ``,
    `/** Fit-to-operator points as a function of applied learned preferences. */`,
    `export function fitFromLessons(applied) {`,
    `  if (applied <= 0) return 5; // no memory → cannot be personalized`,
    `  return Math.min(FIT_WEIGHT, 5 + applied * 7);`,
    `}`,
    ``,
    `/** The four LLM-judged criteria (weights sum to 75; Fit's 25 is computed). */`,
    `export const JUDGED = [`,
    `  { name: "Clarity", weight: 20 },`,
    `  { name: "Actionability", weight: 20 },`,
    `  { name: "Signal selection", weight: 20 },`,
    `  { name: "Grounding", weight: 15 },`,
    `];`,
    ``,
    `/** Parse the four judged scores from the gate model's text, add Fit. */`,
    `export function parseGate(text, appliedLessons) {`,
    `  const criteria = JUDGED.map((c) => {`,
    `    const m = text.match(new RegExp(\`\${c.name}\\\\s*:?\\\\s*(\\\\d{1,3})\`, "i"));`,
    `    const raw = m ? parseInt(m[1], 10) : Math.round(c.weight * 0.8);`,
    `    const score = Math.max(0, Math.min(c.weight, raw));`,
    `    return { name: c.name, score, note: "" };`,
    `  });`,
    ``,
    `  const fit = fitFromLessons(appliedLessons);`,
    `  criteria.push({`,
    `    name: "Fit to operator",`,
    `    score: fit,`,
    `    note:`,
    `      appliedLessons <= 0`,
    `        ? "no accumulated memory yet — cannot personalize"`,
    `        : \`\${appliedLessons} learned preference(s) applied\`,`,
    `  });`,
    ``,
    `  const score = criteria.reduce((s, c) => s + c.score, 0);`,
    `  return { score, pass: score >= PASS_THRESHOLD, criteria, revisionHint: text };`,
    `}`,
    ``,
  ].join("\n");

  // ── engine/tools.mjs — same logic as engine/tools.ts (no imports).
  const toolsMjs = [
    `/**`,
    ` * TOOLS LAYER (5/5) — pure ES module, no imports.`,
    ` * Defaults to dry-run (drafts, not sends) for safety.`,
    ` */`,
    ``,
    `/** Pull the "## Actions" lines ("- [tool] description") out of the brief. */`,
    `export function parseActions(brief) {`,
    `  const out = [];`,
    `  const re = /-\\s*\\[([a-z0-9_.]+)\\]\\s*(.+)/gi;`,
    `  // only scan after an "## Actions" heading if present`,
    `  const idx = brief.search(/##\\s*Actions/i);`,
    `  const scope = idx >= 0 ? brief.slice(idx) : brief;`,
    `  let m;`,
    `  while ((m = re.exec(scope)) !== null) {`,
    `    out.push({ tool: m[1].trim(), desc: m[2].trim() });`,
    `  }`,
    `  return out;`,
    `}`,
    ``,
    `export function act(actions, dryRun = true) {`,
    `  return actions.map((a) => ({`,
    `    tool: a.tool,`,
    `    ok: true,`,
    `    result: dryRun`,
    `      ? \`dry-run · would \${a.tool}: \${a.desc}\``,
    `      : \`executed \${a.tool}\`,`,
    `  }));`,
    `}`,
    ``,
  ].join("\n");

  // ── engine/loop.mjs — the runnable closed loop.
  const sensorsJson = JSON.stringify(spec.sensors);
  const loopMjs = [
    `/**`,
    ` * THE OPERATING LOOP — sensor → policy → tools → gate → learning.`,
    ` * Reads ANTHROPIC_API_KEY, dynamically imports @anthropic-ai/sdk, runs one`,
    ` * pass on a fixture, and writes runs/<ts>/output.md + runs/<ts>/gate.json`,
    ` * plus a lesson to memory/. If the key is missing it exits 0 with a clear`,
    ` * message so anyone can explore the repo safely.`,
    ` */`,
    `import { mkdir, writeFile, readdir } from "node:fs/promises";`,
    `import { existsSync } from "node:fs";`,
    `import { join } from "node:path";`,
    `import { fileURLToPath } from "node:url";`,
    `import { dirname } from "node:path";`,
    `import { fitFromLessons, parseGate, PASS_THRESHOLD } from "./gate.mjs";`,
    `import { parseActions, act } from "./tools.mjs";`,
    ``,
    `const HERE = dirname(fileURLToPath(import.meta.url));`,
    `const ROOT = join(HERE, "..");`,
    `const MODEL = "claude-opus-4-8";`,
    `const SENSORS = ${sensorsJson};`,
    ``,
    `// SENSOR — in a live deployment these come from MCP connectors; here we use a`,
    `// fixture so the loop is runnable end-to-end without external accounts.`,
    `function sensor() {`,
    `  return [`,
    `    {`,
    `      source: SENSORS[0] || "fixture",`,
    `      ts: new Date().toISOString(),`,
    `      actor: "operator",`,
    `      text: "Two prospects went quiet after the demo; one renewal is up in 9 days.",`,
    `    },`,
    `  ];`,
    `}`,
    ``,
    `async function countLessons() {`,
    `  const dir = join(ROOT, "memory");`,
    `  if (!existsSync(dir)) return 0;`,
    `  const files = await readdir(dir);`,
    `  return files.filter((f) => f.endsWith(".md")).length;`,
    `}`,
    ``,
    `async function main() {`,
    `  const key = process.env.ANTHROPIC_API_KEY;`,
    `  if (!key) {`,
    `    console.log(`,
    `      "ANTHROPIC_API_KEY is not set. Set it to run the live loop:\\n" +`,
    `        "  export ANTHROPIC_API_KEY=sk-ant-...\\n" +`,
    `        "Meanwhile, \`npm test\` works with no key and no network.",`,
    `    );`,
    `    process.exit(0);`,
    `  }`,
    ``,
    `  const { default: Anthropic } = await import("@anthropic-ai/sdk");`,
    `  const client = new Anthropic({ apiKey: key });`,
    ``,
    `  const signals = sensor();`,
    `  const applied = await countLessons();`,
    `  const signalText = signals.map((s) => \`- [\${s.source}] \${s.text}\`).join("\\n");`,
    ``,
    `  // POLICY — Opus decides what matters and emits an "## Actions" section.`,
    `  const policy = await client.messages.create({`,
    `    model: MODEL,`,
    `    max_tokens: 1024,`,
    `    system:`,
    `      "You run an operating loop. Decide the single most important focus from " +`,
    `      "the signals, then write a short brief ending with an '## Actions' section " +`,
    `      "where each line is '- [tool] concrete action' (tool e.g. gmail.draft).",`,
    `    messages: [{ role: "user", content: \`Signals:\\n\${signalText}\` }],`,
    `  });`,
    `  const brief = policy.content`,
    `    .filter((b) => b.type === "text")`,
    `    .map((b) => b.text)`,
    `    .join("\\n");`,
    ``,
    `  // TOOLS — dry-run the parsed actions.`,
    `  const outcomes = act(parseActions(brief), true);`,
    ``,
    `  // GATE — Opus grades the four judged criteria; Fit is computed from memory.`,
    `  const gateResp = await client.messages.create({`,
    `    model: MODEL,`,
    `    max_tokens: 512,`,
    `    system:`,
    `      "You are an exacting quality gate. Grade Clarity (max 20), Actionability " +`,
    `      "(max 20), Signal selection (max 20), Grounding (max 15). Output exactly " +`,
    `      "four lines: 'Clarity: N', 'Actionability: N', 'Signal selection: N', " +`,
    `      "'Grounding: N'.",`,
    `    messages: [{ role: "user", content: \`Deliverable:\\n\\n\${brief}\` }],`,
    `  });`,
    `  const gateText = gateResp.content`,
    `    .filter((b) => b.type === "text")`,
    `    .map((b) => b.text)`,
    `    .join("\\n");`,
    `  const gate = parseGate(gateText, applied);`,
    ``,
    `  // LEARNING — reflect one durable lesson into memory/.`,
    `  const ts = new Date().toISOString().replace(/[:.]/g, "-");`,
    `  const runDir = join(ROOT, "runs", ts);`,
    `  await mkdir(runDir, { recursive: true });`,
    `  await writeFile(join(runDir, "output.md"), brief, "utf8");`,
    `  await writeFile(join(runDir, "gate.json"), JSON.stringify(gate, null, 2), "utf8");`,
    ``,
    `  await mkdir(join(ROOT, "memory"), { recursive: true });`,
    `  const lesson = [`,
    `    \`# Lesson — \${ts}\`,`,
    `    \`type: pattern\`,`,
    `    \`trigger: when similar signals appear (\${SENSORS.join(", ")})\`,`,
    `    \`lesson: focus held at score \${gate.score}; reinforce grounded, sendable actions.\`,`,
    `  ].join("\\n");`,
    `  await writeFile(join(ROOT, "memory", \`lesson-\${ts}.md\`), lesson, "utf8");`,
    ``,
    `  console.log(`,
    `    \`Run complete. Score \${gate.score}/100 (\${gate.pass ? "PASS" : "HELD"}, \` +`,
    `      \`threshold \${PASS_THRESHOLD}, fit \${fitFromLessons(applied)}).\\n\` +`,
    `      \`Output: runs/\${ts}/output.md\`,`,
    `  );`,
    `}`,
    ``,
    `main().catch((e) => {`,
    `  console.error("Loop error:", e?.message || e);`,
    `  process.exit(1);`,
    `});`,
    ``,
  ].join("\n");

  // ── health.mjs — tiny health server + --once mode.
  const healthMjs = [
    `/**`,
    ` * HEALTH — GET /health returns the most recent run's status. With --once it`,
    ` * prints the same JSON to stdout and exits (for tests/CI without a port).`,
    ` */`,
    `import { createServer } from "node:http";`,
    `import { readFile, readdir } from "node:fs/promises";`,
    `import { existsSync } from "node:fs";`,
    `import { join, dirname } from "node:path";`,
    `import { fileURLToPath } from "node:url";`,
    ``,
    `const HERE = dirname(fileURLToPath(import.meta.url));`,
    `const RUNS = join(HERE, "runs");`,
    `const LOOP_NAME = ${JSON.stringify(name)};`,
    ``,
    `async function health() {`,
    `  let lastRun = null;`,
    `  let lastScore = null;`,
    `  if (existsSync(RUNS)) {`,
    `    const dirs = (await readdir(RUNS)).filter((d) => !d.startsWith(".")).sort();`,
    `    const latest = dirs[dirs.length - 1];`,
    `    if (latest) {`,
    `      lastRun = latest;`,
    `      const gatePath = join(RUNS, latest, "gate.json");`,
    `      if (existsSync(gatePath)) {`,
    `        try {`,
    `          const gate = JSON.parse(await readFile(gatePath, "utf8"));`,
    `          lastScore = typeof gate.score === "number" ? gate.score : null;`,
    `        } catch {}`,
    `      }`,
    `    }`,
    `  }`,
    `  return { status: "ok", loop: LOOP_NAME, lastRun, lastScore };`,
    `}`,
    ``,
    `if (process.argv.includes("--once")) {`,
    `  console.log(JSON.stringify(await health()));`,
    `  process.exit(0);`,
    `}`,
    ``,
    `const PORT = process.env.PORT || 8787;`,
    `createServer(async (req, res) => {`,
    `  if (req.method === "GET" && req.url === "/health") {`,
    `    res.writeHead(200, { "content-type": "application/json" });`,
    `    res.end(JSON.stringify(await health()));`,
    `    return;`,
    `  }`,
    `  res.writeHead(404, { "content-type": "application/json" });`,
    `  res.end(JSON.stringify({ error: "not found" }));`,
    `}).listen(PORT, () => console.log(\`health on :\${PORT}/health\`));`,
    ``,
  ].join("\n");

  // ── tests/loop.test.mjs — node:test, no install, no network.
  const testMjs = [
    `import { test } from "node:test";`,
    `import assert from "node:assert/strict";`,
    `import { fitFromLessons, PASS_THRESHOLD } from "../engine/gate.mjs";`,
    `import { parseActions } from "../engine/tools.mjs";`,
    ``,
    `test("fitFromLessons: zero memory yields the floor of 5", () => {`,
    `  assert.equal(fitFromLessons(0), 5);`,
    `  assert.equal(fitFromLessons(-3), 5);`,
    `});`,
    ``,
    `test("fitFromLessons: monotonic non-decreasing across applied 0..5", () => {`,
    `  let prev = -1;`,
    `  for (let applied = 0; applied <= 5; applied++) {`,
    `    const fit = fitFromLessons(applied);`,
    `    assert.ok(fit >= prev, \`fit must not decrease at applied=\${applied}\`);`,
    `    prev = fit;`,
    `  }`,
    `});`,
    ``,
    `test("fitFromLessons: caps at 25", () => {`,
    `  assert.equal(fitFromLessons(3), 25); // 5 + 3*7 = 26 → capped`,
    `  assert.equal(fitFromLessons(100), 25);`,
    `  for (let applied = 0; applied <= 50; applied++) {`,
    `    assert.ok(fitFromLessons(applied) <= 25);`,
    `  }`,
    `});`,
    ``,
    `test("PASS_THRESHOLD is 80", () => {`,
    `  assert.equal(PASS_THRESHOLD, 80);`,
    `});`,
    ``,
    `test("parseActions extracts tool + desc from an ## Actions section", () => {`,
    `  const brief = [`,
    `    "Focus: re-engage quiet prospects.",`,
    `    "",`,
    `    "## Actions",`,
    `    "- [gmail.draft] Send a 2-line nudge to the two quiet prospects.",`,
    `    "- [calendar.create] Block 20m to prep the renewal call.",`,
    `  ].join("\\n");`,
    `  const actions = parseActions(brief);`,
    `  assert.equal(actions.length, 2);`,
    `  assert.equal(actions[0].tool, "gmail.draft");`,
    `  assert.ok(actions[0].desc.includes("nudge"));`,
    `  assert.equal(actions[1].tool, "calendar.create");`,
    `});`,
    ``,
    `test("parseActions ignores non-action lines before the Actions heading", () => {`,
    `  const brief = "- [should.ignore] before heading\\n## Actions\\n- [real.tool] do it";`,
    `  const actions = parseActions(brief);`,
    `  assert.equal(actions.length, 1);`,
    `  assert.equal(actions[0].tool, "real.tool");`,
    `});`,
    ``,
  ].join("\n");

  const settingsJson = JSON.stringify(
    { $schema: "https://json.schemastore.org/claude-code-settings.json" },
    null,
    2,
  );

  const gitignore = [
    "node_modules/",
    ".env",
    ".env.*",
    "!.env.example",
    "runs/*",
    "!runs/.gitkeep",
    "memory/*",
    "!memory/.gitkeep",
    "",
  ].join("\n");

  return [
    { path: "loop-spec.json", content: JSON.stringify(spec, null, 2) },
    { path: "CLAUDE.md", content: claudeMd },
    { path: "rubric.md", content: rubricMd },
    { path: "RUNBOOK.md", content: runbook },
    { path: "package.json", content: pkg },
    { path: "engine/gate.mjs", content: gateMjs },
    { path: "engine/tools.mjs", content: toolsMjs },
    { path: "engine/loop.mjs", content: loopMjs },
    { path: "health.mjs", content: healthMjs },
    { path: "tests/loop.test.mjs", content: testMjs },
    { path: ".claude/settings.json", content: settingsJson },
    { path: ".gitignore", content: gitignore },
    { path: "memory/.gitkeep", content: "" },
    { path: "runs/.gitkeep", content: "" },
    { path: ".env.example", content: "ANTHROPIC_API_KEY=\n" },
  ];
}
